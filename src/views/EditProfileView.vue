<template>
    <div class="flex flex-col h-screen bg-gray-50">
        <header class="bg-white border-b border-gray-200 px-4 py-4">
            <div class="flex items-center justify-between">
                <button @click="$router.back()" class="text-gray-600">
                    <svg class="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 19l-7-7 7-7" />
                    </svg>
                </button>
                <h1 class="text-xl font-bold text-gray-800">{{ $t('profile.editProfile') }}</h1>
                <button @click="saveProfile" class="text-primary font-semibold">{{ $t('common.done') }}</button>
            </div>
        </header>
        <main class="flex-1 overflow-y-auto px-4 py-6">
            <div class="flex flex-col items-center mb-8">
                <div class="relative">
                    <div
                        class="w-24 h-24 bg-gradient-to-br from-[#4255FF] to-indigo-600 rounded-full flex items-center justify-center text-4xl text-white shadow-lg overflow-hidden">
                        <img v-if="userStore.profile.avatar" :src="'/assets/avatars/' + userStore.profile.avatar"
                            alt="Avatar" class="w-full h-full object-cover" />
                    </div>
                    <button @click="$router.push('/avatar-shop')"
                        class="absolute bottom-0 right-0 w-8 h-8 bg-white rounded-full shadow-lg flex items-center justify-center">
                        <svg class="w-4 h-4 text-gray-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2"
                                d="M15.232 5.232l3.536 3.536m-2.036-5.036a2.5 2.5 0 113.536 3.536L6.5 21.036H3v-3.572L16.732 3.732z" />
                        </svg>
                    </button>
                </div>
                <button @click="$router.push('/avatar-shop')" class="mt-3 text-sm text-primary font-semibold">{{
                    $t('avatarShop.changeAvatar') }}</button>
            </div>
            <div class="space-y-4">
                <div>
                    <label class="text-sm font-medium text-gray-700 mb-2 block">{{ $t('profile.name') }}</label>
                    <input v-model="userName" type="text"
                        class="w-full text-gray-800 bg-white rounded-xl px-4 py-3 border-2 border-gray-200 focus:border-primary focus:outline-none" />
                </div>
                <div>
                    <label class="text-sm font-medium text-gray-700 mb-2 block">{{ $t('profile.email') }}</label>
                    <input v-model="userEmail" type="email"
                        class="w-full text-gray-800 bg-white rounded-xl px-4 py-3 border-2 border-gray-200 focus:border-primary focus:outline-none" />
                </div>
                <div>
                    <label class="text-sm font-medium text-gray-700 mb-2 block">{{ $t('profile.bio') }}</label>
                    <textarea v-model="userBio" rows="4" :placeholder="$t('profile.bioPlaceholder')"
                        class="w-full text-gray-800 bg-white rounded-xl px-4 py-3 border-2 border-gray-200 focus:border-primary focus:outline-none resize-none"></textarea>
                </div>
                <div>
                    <label class="text-sm font-medium text-gray-700 mb-2 block">{{ $t('profile.dailyGoal') }}</label>
                    <input v-model.number="dailyGoal" type="number" min="1" max="100"
                        class="w-full text-gray-800 bg-white rounded-xl px-4 py-3 border-2 border-gray-200 focus:border-primary focus:outline-none" />
                </div>
            </div>
        </main>
    </div>
</template>

<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { useUserStore } from '@/stores/user'
import { useGoalsStore } from '@/stores/goals'

const router = useRouter()
const userStore = useUserStore()
const goalsStore = useGoalsStore()

const userName = ref('Max Mustermann')
const userEmail = ref('max@example.com')
const userBio = ref('Learning...')
const dailyGoal = ref(20)

onMounted(async () => {
    await userStore.loadFromStorage()
    await goalsStore.loadGoals()
    if (userStore.profile.name) userName.value = userStore.profile.name
    if (userStore.profile.email) userEmail.value = userStore.profile.email
    if (userStore.profile.bio) userBio.value = userStore.profile.bio
    dailyGoal.value = goalsStore.dailyGoal
})

const saveProfile = async () => {
    await userStore.updateProfile({
        name: userName.value,
        email: userEmail.value,
        bio: userBio.value,
        emoji: userStore.profile.avatar
    })

    goalsStore.dailyGoal = dailyGoal.value
    await goalsStore.saveGoals()
    router.back()
}
</script>

<style scoped>
.active\:scale-95:active {
    transform: scale(0.95);
}

header {
    padding-top: env(safe-area-inset-top);
}
</style>